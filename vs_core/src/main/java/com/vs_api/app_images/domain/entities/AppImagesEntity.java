package com.vs_api.app_images.domain.entities;

public class AppImagesEntity {
    private String url;
    private int xAxis;
    private int yAxis;
    private int position;

    public String getUrl() {
        return url;
    }

    public void setUrl(String value) {
        url = value;
    }

    public int getXAxis() {
        return xAxis;
    }

    public void setXAxis(int value) {
        xAxis = value;
    }

    public int getYAXis() {
        return yAxis;
    }

    public void setYAxis(int value) {
        yAxis = value;
    }

    public int getPosition() {
        return position;
    }

    public void setPosition(int value) {
        position = value;
    }
}
